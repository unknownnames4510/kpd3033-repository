public class JavaDataTypes {
    public static void main(String[] args) {
        int number = 5; //integer (Semua nombor)
        float floatnumber = 5; //floating point number 1-5
        double Donumber = 16; //nombor perpuluhan 1-16
        char letter = 'I'; //huruf (1 aksara)
        boolean mybool = true; //boolean (true or false)
        String text = "Hai, saya pengguna java baru"; //string (kumpulan aksara)

        System.out.println(number);
        System.out.println(floatnumber);
        System.out.println(Donumber);
        System.out.println(letter);
        System.out.println(mybool);
        System.out.println(text);  

    }

}

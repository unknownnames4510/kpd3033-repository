package javaapplication1;

public class LatihanIFELSEIFLSE {
    public static void main(String[] args) {
        int nombor = -7;
        if (nombor > 0) {
            System.out.println("Integer tersebut adalah nombor positif.");
        } else if (nombor == 0) {
            System.out.println("Nombor adalah sifar.");
        } else {
            System.out.println("Nombor adalah negatif.");
        }
    }

}
